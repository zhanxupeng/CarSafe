package com.mr.forecast;

public class Forecast {
	private int id;
	private String route;
	private int time;
	private int rrank;
	private int trank;
	private int hrank;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public int getRrank() {
		return rrank;
	}
	public void setRrank(int rrank) {
		this.rrank = rrank;
	}
	public int getTrank() {
		return trank;
	}
	public void setTrank(int trank) {
		this.trank = trank;
	}
	public int getHrank() {
		return hrank;
	}
	public void setHrank(int hrank) {
		this.hrank = hrank;
	}
}
